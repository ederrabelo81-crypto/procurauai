export interface Business {
  id: string;
  name: string;
  category: string;
  categorySlug: string;
  tags: string[];
  neighborhood: string;
  hours: string;
  phone?: string;
  whatsapp: string;
  coverImages: string[];
  isOpenNow: boolean;
  isVerified?: boolean;
  description?: string;
  address?: string;
}

export interface Listing {
  id: string;
  type: 'venda' | 'doacao';
  title: string;
  price?: number;
  neighborhood: string;
  images: string[];
  whatsapp: string;
  createdAt: string;
  description?: string;
  isHighlighted?: boolean;
}

export interface Deal {
  id: string;
  title: string;
  subtitle?: string;
  priceText: string;
  validUntil: string;
  businessId?: string;
  businessName?: string;
  image: string;
  whatsapp: string;
  isSponsored?: boolean;
}

export interface Event {
  id: string;
  title: string;
  dateTime: string;
  location: string;
  priceText: string;
  tags: string[];
  image: string;
  whatsapp?: string;
  description?: string;
}

export interface News {
  id: string;
  title: string;
  tag: string;
  snippet: string;
  date: string;
  image?: string;
  url?: string; // novo: link externo (prefeitura, g1, instagram, etc)
}

export interface Obituary {
  id: string;
  name: string;
  age?: number;
  date: string;
  wakeLocation: string;
  burialLocation: string;
  burialDateTime: string;
  message?: string;
  status: 'pending' | 'approved';
}

